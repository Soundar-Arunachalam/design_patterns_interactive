// Receiver — the actual washing machine being controlled
public class WashingMachine {
    public void on()    { System.out.println("Washing Machine ON");  }
    public void off()   { System.out.println("Washing Machine OFF"); }
    public void spin()  { System.out.println("Washing Machine SPINNING");  }
    public void rinse() { System.out.println("Washing Machine RINSING"); }
    public void dry()   { System.out.println("Washing Machine DRYING");  }
}
